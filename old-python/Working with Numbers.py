# Đọc từ trên xuống nha, ko là ko hiểu đâu

print(2) #số âm luôn

print(2.12332) #số âm luôn

print(3 + 5.2) #cộng trừ nhân chia luôn

print(3 * (5 + 2)) #tương tự mấy cái phương trình khác

print(10 % 3)

my_num = 5
print(str(my_num) + " is my favorite number.") #chuyển đổi sang dãy kí tự (string)
#khi số đi cùng với string thì phải chuyển sang string

my_num1 = -5 # vẫn là my_num nha :D
print(abs(my_num1)) # giá trị tuyệt đối

print(pow(34,2)) # dấu phẩy là dấu mũ (cái này là số mũ)

print(max(6,5)) # số lớn nhất trong ngược đc ngăn cách bởi dấu phẩy

print(min(6,5)) # số nhỏ nhất trong ngược đc ngăn cách bởi dấu phẩy

print(round(5.6)) # làm tròn số (0-4 là làm tròn xuống, 5-9 là làm tròn lên)

from math import *
print(floor(3.7)) # số làm tròn xuống nhỏ nhất (ko tính qui tắc làm tròn số)

print(ceil(3.7)) # số làm tròn lên lớn nhất (ko tính qui tắc làm tròn số)

print(sqrt(36)) # căn bậc hai

